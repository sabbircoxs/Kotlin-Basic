package Oparators

fun main() {
    var a = 120
    var b = 65
    //Addition Oparator(+)
    var sum = a+b
    println("sum = $sum")

    //Subtraction Operator
    var sub = a-b
    println("sub = $sub")

    //Multiplication Operator
    var mult = a *b
    println("mult = $mult")

    //Division Operator
    var div = a / b.toDouble()
    println("div = $div")

    //Modulus Operator
    var mod = a % b
    println("mod = $mod")

}