package Oparators

fun main(){
    var a = 10
    var b = 12
    //a +=b	a = a + b
    a+=b
    println(a)

    //a -= b	a = a - b
    a-=b
    println(a)

    //a *= b	a = a * b
    a*=b
    println(a)

    //  a /= b	a = a / b
    a/=b
    println(a)

    // a %= b	a = a % b
    a%=b
    println(a)
}