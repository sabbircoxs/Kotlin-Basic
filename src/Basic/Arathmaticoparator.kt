package Operators

fun main() {
    var a = 120
    var b = 65
//Addition Operator(+)
    var sum = a + b
    println("Sum: $sum")
//Subtraction Operator
    var sub = a - b
    println("Sub: $sub")
//Multiplication Operator
    var mult = a * b
    println("Mutli: $mult")
//Division Operator
    var div = a / b.toDouble()
    println("Div: $div")
    //Modulus Operator
    var mud = a%b
    println("Mud:$mud")
}